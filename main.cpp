void loadCrops() {
    std::string line;
    std::ifstream myfile("crops.csv");
    int index = 0;

    if (myfile.is_open()) {
        while (std::getline(myfile, line)) {
            std::stringstream ss(line);

            std::getline(ss, fieldId[index], ',');
            std::getline(ss, crops[index], ',');
            std::getline(ss, fieldArea[index], ',');
            std::getline(ss, plantingDate[index], ',');
            std::getline(ss, harvestingDate[index], ',');
            std::getline(ss, growthStage[index], ',');

            index++;
        }
    }
    myfile.close();
}

void saveCrops() {
    std::ofstream myfile;
    myfile.open("crops.csv");

    for (int i = 0; i < MAX; i++) {
        if (fieldId[i] != "\0") {
            myfile << fieldId[i] + ","
                   << crops[i] + ","
                   << fieldArea[i] + ","
                   << plantingDate[i] + ","
                   << harvestingDate[i] + ","
                   << growthStage[i] + "\n";
        } 
        else {
            break;
        }
    }
    myfile.close();
}

