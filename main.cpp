int main() {
    int choice;

    // For ASCII art compatability
    SetConsoleOutputCP(CP_UTF8);
    SetConsoleCP(CP_UTF8);

    // Load csv files
    loadCrops();
    loadLivestock();
    loadInventory();
    loadFinance();

    titleScreen();
    do {
        system("cls");
        mainMenu(choice);
        
        switch (choice) {
            // Crop Management
            case 1:
                system("cls");
                cropManager();
                break;

            // Livestock Management
            case 2: 
                system("cls");
                livestockManager();
                break;

            // Inventory Management
            case 3: 
                system("cls");
                inventoryManager();
                break;

            // Financial Management
            case 4: 
                system("cls");
                financeManager(); 
                break;
                
            // System Overview
            case 5: 
                system("cls");
                systemOverview();
                break;

            // Exit Main Menu
            case 6:
                std::cout << "Thank you for using AgriNote!\n";
                break;

            default:
                std::cout << "Please select a valid option (1-6)\n";
                break;
        }
    } while (choice != 6);


    return 0;
}

