// LIVESTOCK MANAGER

void loadLivestock() {
    std::string line;
    std::ifstream file("livestock.csv");
    int index = 0;

    if (file.is_open()) {
        while (std::getline(file, line)) {
            std::stringstream ss(line);

            std::getline(ss, animalId[index], ',');
            std::getline(ss, species[index], ',');
            std::getline(ss, age[index], ',');
            std::getline(ss, healthStatus[index], ',');
            std::getline(ss, lastCheckup[index], ',');

            index++;
        }
    }
    file.close();
}

void saveLivestock() {
    std::ofstream file("livestock.csv");

    for (int i = 0; i < MAX; i++) {
        if (animalId[i] != "\0") {
            file << animalId[i] << ","
                 << species[i] << ","
                 << age[i] << ","
                 << healthStatus[i] << ","
                 << lastCheckup[i] << "\n";
        } else {
            break;
        }
    }
    file.close();
}

